But it's of course not as simple as that. There's a story behind everything, and our company has one as well.<br><br>

 

Back in the days, when I was an avid guitarist and studio technician I always felt that something was removed from the sonic equation, whenever I plugged my instruments through a chain of effects. Even though the effects were great, and added to my sonic palette, I felt that the most important thing didn't shine through: Namely my original sound signal. The sounds inside my head were so much clearer than in reality, so I simply had to pursue them. When my friend Carl Martin asked if I would help design a guitar pedal for him, I immediately saw this as the chance to prove that something better than what was on the current market could be made. We had no clue of what we were doing, but we knew where we wanted to end up, and the result of this was The Hot Drive N' Boost, which with it's 12V circuitry, introduced a new level of clarity to the effect pedal world.<br>

After that, our company was created, but the vision, and the values behind our very first creation hasn't changed one bit.<br><br>

It's still all about being ambitious, thorough, creative and constantly push the perception of how analog pedals can sound, and more importantly, raise the bar for how sound can and should be perceived. </h4>
